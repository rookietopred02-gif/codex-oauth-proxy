export { I18N_TEXT as DASHBOARD_I18N_TEXT };

export function createDashboardI18n() {
  let uiLang = "en";

  function normalizeUiLang(value) {
    const v = String(value || "").trim().toLowerCase();
    if (["zh-hans", "zh_cn", "zh-cn", "hans", "cn", "sc"].includes(v)) return "zh-hans";
    if (["zh-hant", "zh_tw", "zh-tw", "zh-hk", "hant", "tw", "tc", "zh"].includes(v)) return "zh-hant";
    return "en";
  }

  function setLanguage(value) {
    uiLang = normalizeUiLang(value);
    return uiLang;
  }

  function getLanguage() {
    return uiLang;
  }

  function t(key) {
    const table = I18N_TEXT[uiLang] || I18N_TEXT.en;
    return table[key] ?? I18N_TEXT.en[key] ?? key;
  }

  function tt(key, vars = {}) {
    const template = String(t(key));
    return template.replace(/\{([a-zA-Z0-9_]+)\}/g, (_m, varName) => {
      if (!Object.prototype.hasOwnProperty.call(vars, varName)) return "";
      const value = vars[varName];
      return value === undefined || value === null ? "" : String(value);
    });
  }

  return {
    getLanguage,
    normalizeUiLang,
    setLanguage,
    t,
    tt
  };
}
